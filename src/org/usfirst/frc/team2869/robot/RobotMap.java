package org.usfirst.frc.team2869.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	
	public static int DRIVESTICK = 0;
	public static int FIRESTICK = 1;
	
	public static int ROTATEBUTTON = 2;
	public static int STOPBUTTON = 3;
	public static int HALFBUTTON = 4;
	public static int TURN180BUTTON = 5;
	
    public static int FRONTLEFT = 0;
    public static int FRONTRIGHT = 3;
    public static int REARLEFT = 2;
    public static int REARRIGHT = 1;
}
